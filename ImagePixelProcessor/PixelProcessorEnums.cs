
namespace ImagePixelProcessor;

/// <summary>
/// Named color channels.
/// </summary>
public enum ColorChannel
{
    /// <summary>
    /// Alpha channel.
    /// </summary>
    A,
    /// <summary>
    /// Red channel.
    /// </summary>
    R,
    /// <summary>
    /// Green channel.
    /// </summary>
    G,
    /// <summary>
    /// Blue channel.
    /// </summary>
    B,
    /// <summary>
    /// Red, green and blue channels for simplicity.
    /// </summary>
    RGB
}
